    private void bQuery04ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bQuery04ActionPerformed

        txtId_Pessoas04.setText("");
        txtNome04.setText("");
        txtIniciante04.setText("");
        txtRating04.setText("");
        txtMensagem104.setText("");
        txtMensagem204.setText("");

        try {
            if (cn != null) {
                String strQuery = TxtQuery04.getText();
                PessoasDAO04 pessoaDao = new PessoasDAO04(cn);
                pessoas = pessoaDao.GetPessoaByQuery(strQuery);
                
                if (pessoas.size() > 0) {
                    index = 0;
                    PessoasPO04 pessoaPo = pessoas.get(index);
                    Mostra(pessoaPo);
                    txtMensagem104.setText( "1 de " + pessoas.size() + " registros!");
                    txtMensagem204.setText(pessoas.size() + " registros encontrados!");
                }else{
                    txtMensagem204.setText("0 registros encontrados!");
                }
                
                
            }
        } catch (Exception ex) {
            txtMensagem204.setText(ex.getMessage());
        }

    }//GEN-LAST:event_bQuery04ActionPerformed

