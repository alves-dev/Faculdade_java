    private void Mover(int inc) {
        if (pessoas != null && (index + inc) >= 0 && (index + inc) < pessoas.size()) {
            
            txtIniciante04.setText("");
            
            index = index + inc;
            PessoasPO04 pessoaPo = pessoas.get(index);
            Mostra(pessoaPo);
            txtMensagem104.setText((index + 1) + " de " + pessoas.size() + " registros!");
        }
    }
    
    private void Mostra(PessoasPO04 pessoaPo) {
        if (pessoaPo != null) {
            txtId_Pessoas04.setText(pessoaPo.id_Pessoa.toString());
                    txtNome04.setText(pessoaPo.nome);
                    txtRating04.setText(String.valueOf(pessoaPo.rating));
                    
                    if (pessoaPo.iniciante != null) {
                        if (pessoaPo.iniciante == false) {
                            txtIniciante04.setText("False");                            
                        } else {
                            txtIniciante04.setText("True");
                        }
                    } else {
                        txtIniciante04.setText("Sem informação!");
                    }
        }
        
    }

