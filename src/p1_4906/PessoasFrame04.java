    private void bConsultar04ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConsultar04ActionPerformed

        TxtQuery04.setText("");
        txtId_Pessoas04.setText("");
        txtIniciante04.setText("");
        txtNome04.setText("");
        txtRating04.setText("");
        txtMensagem104.setText("");
        txtMensagem204.setText("");

        try {
            if (cn != null) {
                Integer id_Pessoa = Integer.parseInt(txtId_Pessoas04.getText());
                PessoasDAO04 pessoaDao = new PessoasDAO04(cn);
                PessoasPO04 pessoaPo = pessoaDao.GetPessoaById(id_Pessoa);
                
                if (pessoaPo != null) {
                    txtId_Pessoas04.setText(pessoaPo.id_Pessoa.toString());
                    txtNome04.setText(pessoaPo.nome);
                    txtRating04.setText(String.valueOf(pessoaPo.rating));
                    
                    if (pessoaPo.iniciante != null) {
                        if (pessoaPo.iniciante == false) {
                            txtIniciante04.setText("False");                            
                        } else {
                            txtIniciante04.setText("True");
                        }
                    } else {
                        txtIniciante04.setText("Sem informação!");
                    }
                    
                }
                txtMensagem204.setText("");
            }
        } catch (Exception ex) {
            txtMensagem204.setText(ex.getMessage());
        }
    }//GEN-LAST:event_bConsultar04ActionPerformed

