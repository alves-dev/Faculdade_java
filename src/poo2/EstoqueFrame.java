    public void limparTela() {
        txtMensagem1.setText("");
        txtMensagem2.setText("");
        txtID.setText("");
        txtDescrição.setText("");
        txtEstoque.setText("");
        txtPreco.setText("");
        txtDataCadastro.setText("");
    }

    private void Mover(int inc) {
        if (estoques != null && (index + inc) >= 0 && (index + inc) < estoques.size()) {

            //txtIniciante04.setText("");
            index = index + inc;
            EstoquePO estoquePo = estoques.get(index);
            Mostra(estoquePo);
            //txtMensagem104.setText((index + 1) + " de " + pessoas.size() + " registros!");
        }
    }

    private void Mostra(EstoquePO estoquePo) {
        if (estoquePo != null) {
            txtID.setText(String.valueOf(estoquePo.pro_ID));
            txtDescrição.setText(estoquePo.pro_Descricao.toString());
            txtPreco.setText(estoquePo.pro_Preco.toString());
            txtDataCadastro.setText(String.valueOf(estoquePo.pro_DataCadastro));
            if (estoquePo.pro_Estoque != null) {
                txtEstoque.setText(estoquePo.pro_Estoque.toString());
            }
        }

    }

