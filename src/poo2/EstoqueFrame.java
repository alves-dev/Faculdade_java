    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:

        limparTela();

        try {
            if (cn != null) {
                
                Integer pro_Id = Integer.parseInt(txtID.getText());
                
                EstoqueDAO estoqueDao = new EstoqueDAO(cn);
                EstoquePO estoquePo = estoqueDao.getEstoqueById(pro_Id);

                if (estoquePo != null) {
                    txtID.setText(String.valueOf(estoquePo.pro_ID));
                    txtDescrição.setText(estoquePo.pro_Descricao.toString());
                    txtPreco.setText(estoquePo.pro_Preco.toString());
                    txtDataCadastro.setText(String.valueOf(estoquePo.pro_DataCadastro));
                    if (estoquePo.pro_Estoque != null) {
                        txtEstoque.setText(estoquePo.pro_Estoque.toString());
                    }
                }
                txtMensagem2.setText("");
            }
        } catch (Exception ex) {
            txtMensagem2.setText(ex.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

